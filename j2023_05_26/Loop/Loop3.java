package j2023_05_26.Loop;

public class Loop3 {
    public static void main(String[] args) {
        //2중 반복문, 중첩 반복문
        for(int i=1; i<=10;i++){
            for(int j=1;j<=5;j++){
                System.out.print(j+"번 ");
            }
            System.out.println();
        }
    }
}
